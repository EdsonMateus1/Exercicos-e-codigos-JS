function ePaisagem(largura,altura){
    return largura > altura ?  true : false; //usando um operador ternario
}
console.log(ePaisagem(1920,1080));


/*O operador ternário é uma 
alternativa para substituir o if…else
 em algumas situações por ser um comando bem 
 enxuto. Condição é a condição que será 
 testada. Verdadeiro é o que fazer quando a 
 condição for verdadeira. Falso é o que fazer 
 quando a condição for falsa.*/

 /*A linguagem de programação C (e linguagens 
derivadas) possui o operador ternário ?:, que 
representa uma expressão condicional. Sua sintaxe é:

condição ? valor se for verdareiro : valor se for falso
 
<condição> ? <operação 1> : <operação 2>;*/
